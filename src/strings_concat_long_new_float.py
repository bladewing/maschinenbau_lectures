value = 123.456789

# Default precision
print(f"Default precision: {value}")

# Specified precision to 2 decimal places
print(f"Two decimal places: {value:.2f}")

# Specified precision to 4 decimal places
print(f"Four decimal places: {value:.4f}")

# Scientific notation with 3 decimal places
print(f"Scientific notation: {value:.3e}")
