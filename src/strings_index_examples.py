str = "abcdefghijklmopqrstuvwxyz"

str[3]  # Zeichen an Index 3, 4. Zeichen
str[3:6]  # ab Index 3 bis 6. Zeichen
str[:3]  # alles bis Index 3
str[-5]  # 5. Zeichen von Hinten
str[-5:]  # alles ab dem 5. Zeichen von Hinten

str[::2]  # jedes zweite Zeichen
str[10::2]  # ab Index 10, jedes zweite Zeichen

str[::-1]  # alles Rückwärts
